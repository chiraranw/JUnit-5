public class Arithmatic {

    public int sum(int a, int b) {
        return a + b;
    }

    public double computeCircleArea(double radius) {
        return radius * Math.PI;
    }

    public double divide(int n, int d) {
        return n / d;
    }

    public int multiply(int n, int d) {
        return n * d;
    }
}
